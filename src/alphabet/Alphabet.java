package alphabet;

public class Alphabet {
    int a, b, c, d, e, f, g, h, i, j, k, l,
            m, n, o, p, q, r, s, t, u, v,
            w, x, y, z;

    public int getSum() {
        return a + b + c + d + e + f + g + h + i + j + k + l +
                m + n + o + p + q + r + s + t + u + v +
                w + x + y + z;
    }

    public void setW(int w) {
        this.w = w;
    }

    public void setA(int a) {
        this.a = a;
    }

    public void setS(int s) {
        this.s = s;
    }

    public void setD(int d) {
        this.d = d;
    }
}
